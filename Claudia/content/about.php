<h2>Portrait</h2>

Mit gerade mal 18 Jahren besuchte Claudia Zemp die Schule für Gestaltung in Luzern. Ein Jahr später folgte ihre erste öffentliche Bilderausstellung. Die Ausbildung als Damenschneiderin folgte. Weitere Ausstellungen in Bern und Basel, sowie der Beruf als Herrenmasskonfektionärin schlossen sich an. Trotz des 100% Vollzeitjobs fand sie Zeit, ihr kreatives Potential mit viel Esprit aus zu leben. 

<h2>Ihre Werke</h2>

Seit dem Besuch auf der Insel Stromboli in Italien, mit dem gleichnamigen Vulkan, war Claudia so entzückt über den glitzernden, schwarzen Vulkansand, dass von da an ein Reisegepäck ohne leere Dosen nicht mehr weg zu denken waren. So hat sich bereits ein kleines Orchester an Sand der besuchten Destinationen zusammen gestellt. Diese werden dann in ihren Bildern verarbeitet und geben einen Hauch an Urlaub und Erinnerungen zurück
<br />
Am liebsten lebt sich Claudia auf grossformatigen Leinwänden aus, mit Acryl und Lack. Sie verleiht manchen Werken einen magnetischen oder fluoreszierenden Effekt, der als solchen nicht zu erkennen ist.
Ihre Bilder sind bunt, vielschichtig, strukturiert und lebhaft. Sie wiederspiegeln ihr Wesen und sprechen aus ihrer Seele.
Die Werke passen in moderne Räumlichkeiten: In Häuser, Büros, Empfangshallen sowie in Hotels oder Praxen.
<br />
Sie sind wunderbar um mit den Gedanken auf Reisen zu gehen.
